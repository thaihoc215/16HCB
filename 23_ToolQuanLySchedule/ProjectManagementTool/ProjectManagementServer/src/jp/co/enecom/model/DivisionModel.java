package jp.co.enecom.model;

public class DivisionModel {

	/**
	 * 区分種類コード
	 */
	public String division_kind_code;
	/**
	 * 区分コード
	 */
	public String division_code;
	/**
	 * 区分名
	 */
	public String division_name;

	/**
	 * @return the division_kind_code
	 */
	public String getDivision_kind_code() {
		return division_kind_code;
	}

	/**
	 * @param division_kind_code the division_kind_code to set
	 */
	public void setDivision_kind_code(String division_kind_code) {
		this.division_kind_code = division_kind_code;
	}

	/**
	 * @return the division_code
	 */
	public String getDivision_code() {
		return division_code;
	}

	/**
	 * @param division_code the division_code to set
	 */
	public void setDivision_code(String division_code) {
		this.division_code = division_code;
	}

	/**
	 * @return the division_name
	 */
	public String getDivision_name() {
		return division_name;
	}

	/**
	 * @param division_name the division_name to set
	 */
	public void setDivision_name(String division_name) {
		this.division_name = division_name;
	}
	
	
}
